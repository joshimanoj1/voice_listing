# Helper function to get translated text
def get_text(key, lang="en", **kwargs):
    """
    Retrieve translated text for the given key and language.
    If the key or language is not found, fall back to English or the key itself.
    Supports string formatting with kwargs.
    """
    lang = lang if lang in TRANSLATIONS else "en"
    text = TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS["en"].get(key, key))
    return text.format(**kwargs) if kwargs else text